def parseInput(inp):
    d = dict()
    for ri, row in enumerate(inp.splitlines()):
        for ci, ch in enumerate(row):
            if ch != ".":
                d[(ri, ci)] = ch
    return d, ri+1, ci+1

def printMap(mp, w, h):
    for y in range(h):
        for x in range(w):
            if (y, x) in mp:
                print(mp[(y, x)], end="")
            else:
                print(".", end="")
        print()

def moveEast(mp, w, h):
    newMap = dict()
    for (y, x), v in mp.items():
        if v == ">" and (y, (x+1)%w) not in mp:
            newMap[(y, (x+1)%w)] = ">"
        else:
            newMap[(y, x)] = mp[(y, x)]
    return newMap

def moveSouth(mp, w, h):
    newMap = dict()
    for (y, x), v in mp.items():
        if v == "v" and ((y+1)%h, x) not in mp:
            newMap[((y+1)%h, x)] = "v"
        else:
            newMap[(y, x)] = mp[(y, x)]
    return newMap

